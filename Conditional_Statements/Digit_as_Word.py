if digit == "0":
    word = "zero"
elif digit == "1":
    word = "one"
elif digit == "2":
    word = "two"
elif digit == "3":
    word = "three"
elif digit == "4":
    word = "four"
elif digit == "5":
    word = "five"
elif digit == "6":
    word = "six"
elif digit == "7":
    word = "seven"
elif digit == "8":
    word = "eight"
elif digit == "9":
    word = "nine"
else:
    word = "not a digit"

